// Helper function to get translation
export const getTranslation = (section, key, language) => {
  try {
    return translations[section]?.[key]?.[language] || translations[section]?.[key]?.en || key;
  } catch (error) {
    console.error('Translation error:', error);
    return key;
  }
};

