// Helper function to translate diagnosis content (points and tips)
// More flexible - handles variations and partial matches
export const translateDiagnosisContent = (text, language) => {
  // If no text or English is selected, return original
  if (!text || language === 'en') return text;
  
  // Clean the input text
  const cleanedText = text.trim();
  
  // Try exact match first
  if (diagnosisContentTranslations[cleanedText]?.[language]) {
    return diagnosisContentTranslations[cleanedText][language];
  }
  
  // Try case-insensitive match
  const lowerText = cleanedText.toLowerCase();
  for (const [key, value] of Object.entries(diagnosisContentTranslations)) {
    if (key.toLowerCase() === lowerText) {
      return value[language] || text;
    }
  }
  
  // If still no match found, log for debugging
  if (language === 'ur') {
    console.warn('Translation missing for:', cleanedText);
    console.log('Available translations:', Object.keys(diagnosisContentTranslations).length, 'entries');
  }
  
  // Return original if no translation found
  return text;
};
